//Konstruktor Fungsi menciptakan objek Fungsi baru. Dalam JavaScript setiap fungsi sebenarnya adalah objek Function.

//======= Syntax ==================
// new Function ([arg1[, arg2[, ...argN]],] functionBody)

//========== Keterangan =============
// Parameter
// Arg1, arg2, ... argn
// Nama yang akan digunakan oleh fungsinya sebagai nama argumen formal. 
// Masing-masing harus berupa string yang sesuai dengan identifier JavaScript yang valid atau daftar string semacam itu yang dipisahkan dengan koma; Misalnya "x", "theValue", atau "a, b".
// FungsiBody
// String berisi pernyataan JavaScript yang terdiri dari definisi fungsi.