// Definisi dan Penggunaan
// Metode concat() digunakan untuk menggabungkan dua atau lebih array.
// Metode ini tidak mengubah array yang ada, namun mengembalikan array baru, yang berisi nilai dari array yang digabungkan.

var KelasSatu = ['ayu', 'Budi', 'Adi'];
var kelasDua = ['Tono', 'Tuti'];
var kelasTiga = ['jono', 'kampret'];

var Gabung = KelasSatu.concat(kelasDua, kelasTiga);

console.log(Gabung);